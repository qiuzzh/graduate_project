% --- Executes on button press in pushbutton3.
function pushbutton3_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton3 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
P_T = str2double(get(handles.edit1,'string')); % 发射的峰值功率
G = str2double(get(handles.edit2,'string')); % 天线增益
lamda = str2double(get(handles.edit3,'string')); % 波长
sigma = str2double(get(handles.edit4,'string')); % 目标的雷达散射截面积 
T0 = str2double(get(handles.edit5,'string')); % 热力学温度
B = str2double(get(handles.edit6,'string')); % 带宽
F = str2double(get(handles.edit7,'string')); % 接收机噪声系数
SNR = str2double(get(handles.edit8,'string')); % 信噪比

K = 1.38810^(-23); % 玻尔兹曼常数
R = P_T * G^2 * lamda^2 * sigma / ((4*pi)^3 * SNR * K * T0 * B * F);
R = R^(1/4);
Radar_out = num2str(R);
set(handles.edit9,'string',Radar_out);





